-- Seed script: cached LLM survey recommendations for 129 test users.
-- One record per (user_id, date) — enforced by unique index idx_survey_recommendations_user_date.
-- Generated when users view their survey history (GET /survey/history).
-- ~1/3 of user-day combinations have a cached recommendation (realistic: not every user
-- checks history every day).

WITH numbered_users AS (
    SELECT
        user_id,
        ROW_NUMBER() OVER (ORDER BY user_id) AS rn
    FROM users
    WHERE login LIKE 'user\_%' ESCAPE '\'
),
calendar AS (
    SELECT generate_series(0, 6) AS days_ago
),
candidates AS (
    SELECT
        nu.user_id,
        nu.rn,
        c.days_ago,
        CURRENT_DATE - c.days_ago AS rec_date
    FROM numbered_users nu
    CROSS JOIN calendar c
    -- Keep ~1/3 of combinations: simulates users who opened history on that day
    WHERE (nu.rn * 3 + c.days_ago) % 3 = 0
)
INSERT INTO survey_recommendations (user_id, date, summary, recommendations, creation_datetime)
SELECT
    user_id,
    rec_date,

    (ARRAY[
        'За последний месяц у пользователя наблюдается стабильное настроение на среднем уровне. Уровень тревожности периодически повышается, что может быть связано с рабочей нагрузкой. Ощущение контроля над жизнью остаётся на удовлетворительном уровне.',
        'Пользователь демонстрирует позитивную динамику настроения за прошедший период. Тревожность находится на низком уровне, ощущение контроля высокое. Общее психологическое состояние оценивается как хорошее.',
        'Наблюдается снижение настроения в последние недели. Уровень тревожности повышен, ощущение контроля снижено. Рекомендуется обратить особое внимание на источники стресса и по возможности снизить нагрузку.',
        'Состояние пользователя нестабильно: настроение колеблется от низкого до среднего. Тревожность остаётся на среднем уровне, ощущение контроля варьируется день от дня.',
        'Пользователь демонстрирует устойчивое состояние с небольшими колебаниями. Все показатели находятся в норме, общая динамика положительная.'
    ])[ (rn % 5) + 1 ],

    (ARRAY[
        'Рекомендуется практиковать ежедневную медитацию по 10–15 минут для снижения тревожности. Обратите внимание на режим сна и старайтесь ложиться в одно и то же время. Физическая активность 3 раза в неделю поможет стабилизировать настроение.',
        'Продолжайте в том же духе! Для поддержания хорошего состояния рекомендуется вести дневник благодарности и отмечать ежедневные достижения. Не забывайте про социальные связи — общение с близкими укрепляет эмоциональный фон.',
        'Рекомендуется снизить нагрузку и расставить приоритеты. Используйте технику «5 минут»: если задача кажется невыполнимой, начните делать её всего 5 минут. Практикуйте дыхательные упражнения при появлении тревожных мыслей.',
        'Обратите внимание на качество отдыха. Попробуйте технику прогрессивной мышечной релаксации перед сном. Ограничьте использование гаджетов за час до сна. Планируйте небольшие приятные активности на каждый день.',
        'Для стабилизации эмоционального состояния рекомендуется вести регулярные записи о своих чувствах. Практикуйте осознанность (mindfulness) в повседневных делах. Рассмотрите возможность консультации со специалистом для более глубокой работы.'
    ])[ (rn % 5) + 1 ],

    -- Cached shortly after the user viewed history (9–11 AM on that day)
    rec_date::timestamptz
        + (9 + (rn % 3)) * INTERVAL '1 hour'
        + ((rn * days_ago * 11) % 60) * INTERVAL '1 minute'

FROM candidates
ORDER BY user_id, rec_date;
